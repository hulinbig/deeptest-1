def sum(a, b):
    c = a + b
    return c

def multi():
    data = []
    for i in range(1, 10):
        line = []
        for j in range(i, 10):
            line.append(u"%d * %d = %2d" % (i, j, i*j))
        data.append(line)
    return data

def sum_tuple(seq):
    sum = 0
    for s in seq:
        sum = sum + s
    return sum

def str_join(str1, str2, str3):
    return str1 + str2 + str3

