# 字典的内置函数示例

if __name__ == "__main__":
    dict_demo = {"1": "初学者", "2": "python"}

    # 计算字典的长度
    print(len(dict_demo))

    # 将字典转换成字符串
    str_demo = str(dict_demo)
    print(dict_demo)
    print(str_demo)

    # 判断类型
    print(type(dict_demo))  # 字典类型
    print(type(str_demo))  # 字符串str类型
